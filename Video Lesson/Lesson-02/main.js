// Basic of Math

let sum;
sum = 2 + 2;
console.log(sum);

sum = 10 - 3;
console.log(sum);

sum = 10 * 3;
console.log(sum);

sum = 10 / 2;
console.log(sum);

sum = 2 + 2 + 2;
console.log(sum);

sum = 2.2 + 2.2;
console.log(sum);

sum = 10.9 * 2 + 20.95;
console.log(sum);

sum = 10.9 * 2 + 20.95 + 4.99;
console.log(sum);

// Order of operations

sum = 1 + 1 * 3;
console.log(sum);

sum = (1 + 1) * 3;
console.log(sum);

sum = 20.95 + 7.99 * 2;
console.log(sum);

sum = (20.95 + 7.99 * 2) * 0.1;
console.log(sum);

// Inaccuracies with floats

sum = 0.1 + 0.2;
console.log(sum);

sum = 20.95 + 7.99;
console.log(sum);

sum = (2095 + 799) / 100;
console.log(sum);

// How to round numbers:

sum = Math.round(2.2);
document.write(sum);

sum = Math.round(2.8);
document.write(sum);

sum = ((2095 + 799) * 0.1) / 100;
document.write(sum);
sum = Math.round((2095 + 799) * 0.1) / 100;
